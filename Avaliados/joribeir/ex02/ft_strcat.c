char	*ft_strcat(char *dest, char *src)
{
	char *ptr;
	char *secptr;

	ptr = dest;
	secptr = src;
	while (ptr[0] != '\0')
		ptr++;

	while (secptr[0] != '\0')
	{
		ptr[0] = secptr[0];
		secptr++;
		ptr++;
	}
	ptr[0] = '\0';
	return (dest);
}

